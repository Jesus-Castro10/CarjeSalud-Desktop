    private void horas(JComboBox cbb) {
        cbb.addItem(LocalTime.of(7, 0).toString());
        for (int hora = 7; hora <= 17; hora++) {
            cbb.addItem(LocalTime.of(hora, 30).toString());
        }
    }

    private void llenarCbbDoctor(String especialidad) {
        java.util.List<Doctor> doctores = doctorService.listarDoctoresEspecialidad(especialidad);
        mapa.clear();
        cbbModificarDoctorCita.removeAllItems();
        for (Doctor doctor : doctores) {
            cbbModificarDoctorCita.addItem(doctor.getFullName());
            mapa.put(doctor.getFullName(), doctor.getNumeroDocumento());
        }
    }

    private JScrollPane listaDoctores(String[] items) {

        jListDoctores.setModel(new javax.swing.AbstractListModel<String>() {
            String[] strings = items;

            @Override
            public int getSize() {
                return strings.length;
            }

            @Override
            public String getElementAt(int i) {
                return strings[i];
            }
        });
        scrollPaneDoctores.setBackground(Color.BLACK);
        scrollPaneDoctores.setViewportView(jListDoctores);
        JScrollBar scrollBar = scrollPaneDoctores.getVerticalScrollBar();
        scrollBar.setBackground(Color.red);
        scrollPaneDoctores.setVerticalScrollBar(scrollBar);
        return scrollPaneDoctores;
    }

