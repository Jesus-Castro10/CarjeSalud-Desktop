    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String doctorId, pacienteId, tipo, consultorio, hora;
        if (txtModificarPacienteCita.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione una cita");
            return;
        }
        String key = cbbModificarDoctorCita.getSelectedItem().toString();
        doctorId = mapa.get(key);
        pacienteId = txtModificarPacienteCita.getText();
        Doctor doctor = doctorService.encontrarDoctor(doctorId);
        Paciente paciente = pacienteService.encontrarPaciente(pacienteId);
        Integer idCita = Integer.valueOf(txtModificarIdCita.getText());
        hora = cbbModificarHora.getSelectedItem().toString();
        consultorio = cbbModificarConsul.getSelectedItem().toString();
        tipo = txtTipo.getText();
        Date fecha = dcModificarFechaCita.getDate();
        Cita cita = new Cita(fecha, hora, tipo, doctor, paciente, consultorio);
        cita.setIdCita(idCita);
        java.util.List<Cita> citas = citaService.listarCitasDoctor(doctor);
        for (Cita cita1 : citas) {
            if (cita1.equals(cita)) {
                JOptionPane.showMessageDialog(this, "No se puede agendar la cita porque algunos datos coinciden con otra");
                return;
            }
        }
        citaService.actualizarCita(cita);
        btnReloadModficarCitaActionPerformed(null);
        JOptionPane.showMessageDialog(this, "Cita actualizada");
        tablaListadoCitas.clearSelection();
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        Persona person = new Paciente();
        Paciente paciente;
        if (getDataForm(txtDocumentoR, txtPriNombreR, txtSegNombreR,
                txtPriApellidoR, txtSegApellidoR, txtEmailR, txtCelularR,
                dcFechaNacimiento, cbSexo, cbEps, uriImg, person)) {
            paciente = (Paciente) person;
        } else {
            JOptionPane.showMessageDialog(this, "Rellene todos los campos");
            return;
        }
        if (pacienteService.encontrarPaciente(paciente.getNumeroDocumento()) != null) {
            JOptionPane.showMessageDialog(this, "Paciente ya se encuentra registrado");
            return;
        }
        pacienteService.guardarPaciente(paciente);
        JOptionPane.showMessageDialog(this, "Paciente registrado exitosamente", "Registro de pacientes",
                JOptionPane.PLAIN_MESSAGE,
                fv.crearIcono("/imagenes/pacientes.png"));
        limpiaFormPaciente();
    }//GEN-LAST:event_jButton7ActionPerformed

