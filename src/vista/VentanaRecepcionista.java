    private void txtBuscarCitaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarCitaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String codigo;
            if (!checkField(txtBuscarCita)) {
                codigo = txtBuscarCita.getText();
                for (int i = 0; i < tablaListadoCitas.getRowCount(); i++) {
                    String sCodigo = tablaListadoCitas.getValueAt(i, 0).toString();
                    if (sCodigo.equalsIgnoreCase(codigo)) {
                        tablaListadoCitas.setRowSelectionInterval(i, i);
                        txtBuscarCita.setText("");
                        tablaListadoCitasMouseClicked(null);
                        return;
                    }
                }
            }
        }
    }//GEN-LAST:event_txtBuscarCitaKeyPressed

