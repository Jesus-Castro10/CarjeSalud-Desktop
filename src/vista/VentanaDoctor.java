    private String extraerMedicamentos() {
        JSONArray medi = new JSONArray();
        Set<String> keys = mapMedicamentos.keySet();
        for (String key : keys) {
            String[] m = mapMedicamentos.get(key);
            medi.put(new JSONObject()
                    .put("cantidad", m[0])
                    .put("medicamento", m[1])
                    .put("posologia", m[2]));
        }
        return medi.toString();
    }

    private String extraerDatosCita(Cita cita) {
        JSONObject datos = new JSONObject()
                .put("doctor", cita.getDoctor().getNumeroDocumento())
                .put("fecha", formatearFecha(cita.getFecha()) + "/" + cita.getHora())
                .put("tipo", cita.getTipo())
                .put("motivo", cita.getMotivo());
        return datos.toString();
    }

    private String extraerEstadoPaciente() {
        String edad, peso, estatura, disasolica, sistolica, pulso, temperatura;
        edad = txtEdad.getText();
        peso = txtPeso.getText();
        estatura = txtEstatura.getText();
        disasolica = txtDisasolica.getText();
        sistolica = txtSistolica.getText();
        pulso = txtFrCardiaca.getText();
        temperatura = txtTemperatura.getText();
        JSONObject datos = new JSONObject()
                .put("edad", edad)
                .put("peso", peso)
                .put("estatura", estatura)
                .put("disasolica", disasolica)
                .put("sistolica", sistolica)
                .put("temperatura", temperatura)
                .put("pulso", pulso);
        return datos.toString();
    }

