    private void mostrarPacientesEnTabla(Doctor doctor) {
        DefaultTableModel modelo = (DefaultTableModel) tbPacientesCita.getModel();
        List<Cita> citas = citaService.listarCitasDoctor(doctor);

        modelo.setRowCount(0);

        String fechaActual = formatearFecha(new Date());
        for (Cita cita1 : citas) {
            String fechaCita = formatearFecha(cita1.getFecha());
            if (fechaCita.equals(fechaActual)) {
                Object[] fila = {
                    cita1.getIdCita(),
                    cita1.getHora(),
                    cita1.getPaciente().getNumeroDocumento(),
                    cita1.getPaciente().getPrimerNombre(),
                    cita1.getPaciente().getPrimerApellido(),
                    cita1.getPaciente().getEps(),};
                modelo.addRow(fila);
            }
        }
        tbPacientesCita.setModel(modelo);
    }

