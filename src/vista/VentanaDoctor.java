    private void btnAddMedicamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMedicamentoActionPerformed
        if (listModel.getSize() >= 5) {
            JOptionPane.showMessageDialog(this, "Solo se puede recetar un maximo\nde 5 medicamentos");
            return;
        }
        String posologia = JOptionPane.showInputDialog("Posologia");
        int cantidad = Integer.parseInt(jsCantidadMedicamento.getValue().toString());
        String sCantidad = jsCantidadMedicamento.getValue().toString();
        String medicamento = txtMedicamento.getText();
        if (cantidad < 1 || medicamento.isBlank() || posologia.isBlank() || isNumeric(posologia) || isNumeric(medicamento)) {
            JOptionPane.showMessageDialog(this, "Error al agregar medicamento\nIntente nuevamente");
            txtMedicamento.setText("");
            jsCantidadMedicamento.setValue(1);
            return;
        }
        String[] value = {sCantidad, medicamento, posologia};
        c++;
        String key = c + "";
        mapMedicamentos.put(key, value);
        String datos = cantidad + " - " + medicamento + " - " + posologia;
        listModel.addElement(datos);
        listaMedicamentos.setModel(listModel);
        txtMedicamento.setText("");
        jsCantidadMedicamento.setValue(1);
    }//GEN-LAST:event_btnAddMedicamentoActionPerformed

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        String message = "¿Desea finalizar la consulta?";
        String path = "/imagenes/question.png";
        int r = JOptionPane.showConfirmDialog(this, message, "Finalizar consulta", 0, 0, fv.crearIcono(path));
        String diagnostico, datosCita, medicamentos, estadoPaciente;
        if (r == JOptionPane.YES_OPTION) {
            Paciente paciente = cita.getPaciente();
            medicamentos = extraerMedicamentos();
            datosCita = extraerDatosCita(cita);
            estadoPaciente = extraerEstadoPaciente();
            diagnostico = areaDiagnostico.getText();
            Historia historiaClinica = new Historia(paciente, new Date(), medicamentos, estadoPaciente, diagnostico, datosCita);
            clinicaService.guardarHistoriaClinica(historiaClinica);
            citaService.eliminarCita(cita);
            JOptionPane.showMessageDialog(this, "Consulta Finalizada");
            limpiarCampos();
            jTabbedPane1.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btnFinalizarActionPerformed

