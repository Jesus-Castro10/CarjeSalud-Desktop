    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String numeroDocumento = txtNumDocR.getText();
        Recepcionista recepcionista = recepcionistaService.encontrarRecepcionista(numeroDocumento);
        int respuesta = JOptionPane.showConfirmDialog(this, "Confirmar eliminacion. \nRecepcionista : "
            + recepcionista.getPrimerNombre() + " " + recepcionista.getPrimerApellido());
        if (respuesta == JOptionPane.YES_OPTION) {
            recepcionistaService.eliminarRecepcionista(recepcionista);
            JOptionPane.showMessageDialog(this, "Recepcionista Eliminado");
            this.dispose();
        }    
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        Persona persona = new Recepcionista();
        Recepcionista recepcionista;
        if (!getDataForm(txtNumDocR, txtPriNombreR, txtSegNombreR, txtPriApellidoR, 
                txtSegApellidoR, txtEmailR, txtCelularR, dcFechaNacimientoR, 
                cbSexoR, cbEpsR, uriImg, persona)) {
            JOptionPane.showMessageDialog(this, "Rellene todos los campos");
            return;
        }
        recepcionista = (Recepcionista) persona;
        recepcionista.setCodigo(codigo);
        if (recepcionistaService.encontrarRecepcionista(recepcionista.getNumeroDocumento()) == null) {
            JOptionPane.showMessageDialog(this, "Recepcionista no se encuentra registrado");
            return;
        }
        recepcionistaService.actualizarRecepcionista(recepcionista);
        limpiar(txtNumDocR, txtPriNombreR, txtSegNombreR, txtPriApellidoR, txtSegApellidoR, txtEmailR, txtCelularR);
        limpiar(dcFechaNacimientoR);
        JOptionPane.showMessageDialog(this, "Datos Actualizados con exito");
        this.dispose();
    }//GEN-LAST:event_jButton9ActionPerformed

