    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        Persona persona = new Doctor();
        Doctor doctor;
        if (!getDataForm(txtNumDocD, txtPriNombreD, txtSegNombreD, txtPriApellidoD, 
                txtSegApellidoD, txtEmailD, txtCelularD, dcFechaNacimientoD, cbSexoD,
                cbEpsD, uriImg, persona)) {
            JOptionPane.showMessageDialog(this, "Rellene todos los campos");
            return;
        }
        doctor = (Doctor) persona;
        String especialidad = cbEspecialidad.getSelectedItem().toString();
        doctor.setCodigo(codigo);
        doctor.setEspecialidad(especialidad);
        if (doctorService.encontrarDoctor(doctor.getNumeroDocumento()) == null) {
            JOptionPane.showMessageDialog(this, "Doctor no se encuentra registrado");
            return;
        }

        doctorService.actualizarDoctor(doctor);
        limpiar( txtNumDocD, txtPriNombreD, txtSegNombreD, txtPriApellidoD, txtSegApellidoD, txtEmailD, txtCelularD);
        limpiar(dcFechaNacimientoD);
        JOptionPane.showMessageDialog(this, "Datos Actualizados con exito");
        this.dispose();
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String numeroDocumento = txtNumDocD.getText();
        Doctor doctor = doctorService.encontrarDoctor(numeroDocumento);
        int respuesta = JOptionPane.showConfirmDialog(this, "Confirmar eliminacion. \nDoctor : "
                + doctor.getPrimerNombre() + " " + doctor.getPrimerApellido());
        if (respuesta == JOptionPane.YES_OPTION) {
            doctorService.eliminarDoctor(doctor);
            JOptionPane.showMessageDialog(this, "Doctor Eliminado");
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

