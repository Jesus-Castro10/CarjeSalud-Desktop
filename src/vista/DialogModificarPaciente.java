    private void llenarDatos(Paciente paciente) {
        if (paciente.getFotoPerfil() != null) {
            lblImgUser.setIcon(cargarImagen(paciente, lblImgUser));
        }
        txtDocumento.setText(paciente.getNumeroDocumento());
        txtPrimerNombre.setText(paciente.getPrimerNombre());
        txtSegundoNombre.setText(paciente.getSegundoNombre());
        txtPrimerApellido.setText(paciente.getPrimerApellido());
        txtSegundoApellido.setText(paciente.getSegundoApellido());
        LocalDate date = paciente.getFechaNacimiento().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        calcularFecha(dcFechaNacimiento, date);
        cbSexo.setSelectedItem(paciente.getSexo());
        cbEps.setSelectedItem(paciente.getEps());
        txtEmail.setText(paciente.getEmail());
        txtCelular.setText(paciente.getCelular());
    }

