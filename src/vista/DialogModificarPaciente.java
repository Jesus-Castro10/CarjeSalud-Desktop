    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Persona person = new Paciente();
        Paciente paciente;
        if(getDataForm(txtDocumento, txtPrimerNombre, txtSegundoNombre,
                txtPrimerApellido, txtSegundoApellido, txtEmail, txtCelular,
                dcFechaNacimiento, cbSexo, cbEps, uriImg, person)){
                paciente = (Paciente) person;
        }else {
            JOptionPane.showMessageDialog(this, "Rellene todos los campos");
            return;
        }
        if (uriImg == null) {
            paciente.setFotoPerfil(null);
        }
        pacienteService.actualizarPaciente(paciente);
        JOptionPane.showMessageDialog(this, "Paciente Actualizado");
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

